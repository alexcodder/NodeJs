import mongoose from "mongoose";

const taskModel = new mongoose.Schema(
  {
    id: {
      type: Number,
      required: true,
    },
    name: {
      type: String,
      required: true,
    },
    isActive: {
      type: Boolean,
      default: true,
    },
  },
  { timestamps: true }
);

const task = mongoose.model("TestModel", taskModel);



export default task;
